// Суть программы: пользователь вводит 3 числа, первое число слагается
// со вторым, а 3 вычитает n из суммы первых 2
    .global main
main:
    call readi32 // Считывает 1 значение в регистр %eax
    mov %eax, %edx

    call readi32 // Считывает 2 значение

    // Складываем 2 числа
    add %edx, %eax
    // Печатаем результат
    call writei32
    call finish
